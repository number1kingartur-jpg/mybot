export function getGuide(slug: string): Guide | undefined {
  const key = slug
    .replace(/^guide_/, "")
    .replace(/-/g, "")
    .toLowerCase();
  const map: Record<string, string> = {
    "7day": "7-day-cut",
    "7daycut": "7-day-cut",
    "7d": "7-day-cut",
    "7mistakes": "7-gym-mistakes",
    "7gym": "7-gym-mistakes",
    "kbju": "kbju-start",
    "nutrition": "kbju-start",
  };
  const resolved = map[key] ?? slug;
  return GUIDES.find((g) => g.slug === resolved || g.slug === slug);
}
